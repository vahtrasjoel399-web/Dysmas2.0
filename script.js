/* ===== HEADER SCROLL ===== */
function initHeader() {
    const header = document.getElementById("header");
    let lastScroll = 0;

    window.addEventListener("scroll", () => {
        const currentScroll = window.scrollY;
        if (currentScroll > 50) {
            header.classList.add("scrolled");
        } else {
            header.classList.remove("scrolled");
        }
        lastScroll = currentScroll;
    }, { passive: true });
}

/* ===== MOBILE NAV ===== */
function initMobileNav() {
    const burger = document.getElementById("burger");
    const nav = document.getElementById("nav");

    burger.addEventListener("click", () => {
        burger.classList.toggle("active");
        nav.classList.toggle("open");
        document.body.style.overflow = nav.classList.contains("open") ? "hidden" : "";
    });

    nav.querySelectorAll(".nav__link").forEach(link => {
        link.addEventListener("click", () => {
            burger.classList.remove("active");
            nav.classList.remove("open");
            document.body.style.overflow = "";
        });
    });
}

/* ===== LANGUAGE SWITCHER ===== */
function initLangSwitcher() {
    const btn = document.getElementById("langBtn");
    const dropdown = document.getElementById("langDropdown");
    const switcher = btn.closest(".lang-switcher");
    const options = dropdown.querySelectorAll(".lang-switcher__option");

    btn.addEventListener("click", (e) => {
        e.stopPropagation();
        switcher.classList.toggle("open");
    });

    document.addEventListener("click", () => {
        switcher.classList.remove("open");
    });

    options.forEach(option => {
        option.addEventListener("click", () => {
            const lang = option.dataset.lang;
            setLanguage(lang);
            switcher.classList.remove("open");

            options.forEach(o => o.classList.remove("active"));
            option.classList.add("active");

            btn.querySelector(".lang-switcher__current").textContent = lang.toUpperCase();
        });
    });
}

function setLanguage(lang) {
    currentLang = lang;
    const t = translations[lang];
    if (!t) return;

    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        if (t[key]) {
            el.innerHTML = t[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (t[key]) {
            el.placeholder = t[key];
        }
    });
}

/* ===== REVEAL ANIMATIONS ===== */
function initRevealAnimations() {
    const reveals = document.querySelectorAll(".reveal");

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add("visible");
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.15,
        rootMargin: "0px 0px -50px 0px"
    });

    reveals.forEach(el => observer.observe(el));
}

/* ===== STATS COUNTER ===== */
function initStatsCounter() {
    const numbers = document.querySelectorAll(".stats__number[data-count]");
    let animated = false;

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !animated) {
                animated = true;
                animateCounters(numbers);
                observer.disconnect();
            }
        });
    }, { threshold: 0.5 });

    const statsSection = document.querySelector(".stats");
    if (statsSection) observer.observe(statsSection);
}

function animateCounters(numbers) {
    numbers.forEach(num => {
        const target = parseInt(num.dataset.count);
        const duration = 2000;
        const step = target / (duration / 16);
        let current = 0;

        const timer = setInterval(() => {
            current += step;
            if (current >= target) {
                current = target;
                clearInterval(timer);
            }
            num.textContent = Math.floor(current);
        }, 16);
    });
}

/* ===== GALLERY MODAL ===== */
function initGalleryModal() {
    const modal = document.getElementById("galleryModal");
    const modalImg = document.getElementById("modalImg");
    const modalClose = document.getElementById("modalClose");
    const track = document.getElementById("galleryTrack");

    if (!track || !modal) return;

    track.querySelectorAll(".gallery__item img").forEach(img => {
        img.addEventListener("click", () => {
            const src = img.src.replace("w=500", "w=1200");
            modalImg.src = src;
            modal.classList.add("active");
            document.body.style.overflow = "hidden";
            track.style.animationPlayState = "paused";
        });
    });

    function closeModal() {
        modal.classList.remove("active");
        document.body.style.overflow = "";
        track.style.animationPlayState = "";
    }

    modalClose.addEventListener("click", closeModal);
    modal.addEventListener("click", (e) => {
        if (e.target === modal) closeModal();
    });
    document.addEventListener("keydown", (e) => {
        if (e.key === "Escape") closeModal();
    });
}

/* ===== CONTACT FORM ===== */
function initContactForm() {
    const form = document.getElementById("contactForm");
    if (!form) return;

    form.addEventListener("submit", (e) => {
        e.preventDefault();

        const name = form.querySelector("#name").value.trim();
        const email = form.querySelector("#email").value.trim();
        const message = form.querySelector("#message").value.trim();

        if (!name || !email || !message) return;

        // Show success state
        const btn = form.querySelector('button[type="submit"]');
        const originalText = btn.textContent;
        btn.textContent = currentLang === "ru" ? "Отправлено ✓" : currentLang === "en" ? "Sent ✓" : "Saadetud ✓";
        btn.style.background = "var(--color-success)";
        btn.style.borderColor = "var(--color-success)";
        btn.disabled = true;

        setTimeout(() => {
            form.reset();
            btn.textContent = originalText;
            btn.style.background = "";
            btn.style.borderColor = "";
            btn.disabled = false;
        }, 3000);
    });
}

/* ===== SMOOTH SCROLL ===== */
function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener("click", function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute("href"));
            if (target) {
                target.scrollIntoView({ behavior: "smooth" });
            }
        });
    });
}

/* ===== ACTIVE NAV ===== */
function initActiveNav() {
    const sections = document.querySelectorAll("section[id]");
    const navLinks = document.querySelectorAll(".nav__link");

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const id = entry.target.id;
                navLinks.forEach(link => {
                    link.classList.toggle("active", link.getAttribute("href") === `#${id}`);
                });
            }
        });
    }, {
        threshold: 0.3,
        rootMargin: "-80px 0px -50% 0px"
    });

    sections.forEach(section => observer.observe(section));
}
